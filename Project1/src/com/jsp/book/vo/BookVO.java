package com.jsp.book.vo;

public class BookVO {
	private String bookCode;
	private String bookSortCode;
	private String bookSortName;
	private String bookPublisher;
	private String bookWriter;
	private String bookPrice;
	private String bookRental;
	public String getBookCode() {
		return bookCode;
	}
	public void setBookCode(String bookCode) {
		this.bookCode = bookCode;
	}
	public String getBookSortCode() {
		return bookSortCode;
	}
	public void setBookSortCode(String bookSortCode) {
		this.bookSortCode = bookSortCode;
	}
	public String getBookSortName() {
		return bookSortName;
	}
	public void setBookSortName(String bookSortName) {
		this.bookSortName = bookSortName;
	}
	public String getBookPublisher() {
		return bookPublisher;
	}
	public void setBookPublisher(String bookPublisher) {
		this.bookPublisher = bookPublisher;
	}
	public String getBookWriter() {
		return bookWriter;
	}
	public void setBookWriter(String bookWriter) {
		this.bookWriter = bookWriter;
	}
	public String getBookPrice() {
		return bookPrice;
	}
	public void setBookPrice(String bookPrice) {
		this.bookPrice = bookPrice;
	}
	public String getBookRental() {
		return bookRental;
	}
	public void setBookRental(String bookRental) {
		this.bookRental = bookRental;
	}
	
	
	
}
